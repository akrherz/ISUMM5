C HEMISPHERE CALCULATION FOR MPP VERSION.  NEED TO COLLECT
C THE XLAT ARRAY ON 1 PROCESSOR (0) TO GET CORRECT LOGICAL
C CENTER OF THE DOMAIN.  THEN BROADCAST THE RESULT.
C DECLARATIONS OF GLEN AND LLEN ARE ADDED AUTOMATICALLY WHEN
C FLIC GENERATES THE INCLUDE TO RSLCOM.INC.
      SUBROUTINE MP_SHEMI(INEST,XLAT,ISN)
#include <parame.incl>
#include <param2.incl>
      REAL XLAT_G(MIX_G*MJX_G)
      INTEGER GLEN_X(2)
      IL = NESTIX(INEST)
      JL = NESTJX(INEST)
      ICENT = IL/2
      JCENT = JL/2
      GLEN_X(1) = IL
      GLEN_X(2) = JL
C INTERNAL WRITE TO GLOBALLY DIMENSIONED ARRAY ON MONITOR PROCESSOR
      CALL RSL_WRITE(XLAT_G,IO2D_IJ_INTERNAL,XLAT,DOMAINS(INEST),
     +               RSL_REAL,GLEN_X,LLEN)
C CALCULATION ON MONITOR PROCESSOR
      IF (RSL_IAMMONITOR()) THEN
        IF(XLAT_G(ICENT+(JCENT-1)*GLEN_X(1)).LT.0.0)ISN=3-ISN
      ENDIF
C RESULT (ISN) BACK TO OTHER PROCESSORS
      CALL RSL_MON_BCAST(ISN,IWORDSIZE)
      RETURN
      END

