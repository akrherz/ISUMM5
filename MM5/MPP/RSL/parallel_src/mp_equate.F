      SUBROUTINE EQUATE(FIN,IXQ,JXQ,KXQ,FOUT,IXI,JXI,KXI)
      IMPLICIT NONE
C
C     PURPOSE  PUT VALUES OF F-IN INTO F-OUT, BOTH HAVE SAME SIZE
C
      INTEGER IXI,JXI,KXI
C ignore these
      INTEGER IXQ,JXQ,KXQ
      REAL    FIN(IXI,JXI,KXI),FOUT(IXI,JXI,KXI)
      INTEGER I,J,K
C
C ON MPP THIS IS JUST LOCAL SUBDOMAIN COPY
C
      DO 100 K=1,KXI
        DO 100 J=1,JXI
          DO 100 I=1,IXI
            FOUT(I,J,K)=FIN(I,J,K)
  100     CONTINUE
      RETURN
      END

